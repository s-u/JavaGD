#define warea 32768

static unsigned char work[warea];

/* convert symbol encoding to UTF8.
   returns a static buffer limited to 32k */
char *symbol2utf8(const char *c) {
  unsigned char *t = work;
  while (*c) {
    if (*c<32) {
      *t=' '; t++;
    } else {
      int u = s2u[*((unsigned char*)c) - 32];
      if (u<128) {
	*t=u; t++;
	
      } else if (u<0x800) {
	*t=0xc0|(u>>6); t++;
	*t=0x80|(u&0x3f); t++;
      } else {
	*t=0xe0|(u>>12); t++;
	*t=0x80|((u>>6)&0x3f); t++;
	*t=0x80|(u&0x3f); t++;
      } /* unicode > 0xffff are not supported, but not needed here, either */
    }
    if (t-work>(warea-6)) break;
    c++;
  }
  *t=0;
  return (char*) work;
}
